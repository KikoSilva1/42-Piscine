#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	li;
	int	j;

	li = argc - 1;
	while (li >= 1)
	{
		j = 0;
		while (argv[li][j])
		{
			write(1, &argv[li][j], 1);
			j++;
		}
		li--;
		write(1, "\n", 1);
	}
}
